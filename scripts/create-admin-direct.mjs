/**
 * Generates scripts/seed_master_admin.sql — a FK-safe, idempotent script that
 * (re)creates the master-admin account directly in D1. This is the reliable
 * path for environments where the bootstrap endpoint isn't reachable (the
 * deploy API token lacks Workers Scripts:Edit, so `wrangler secret put
 * BOOTSTRAP_TOKEN` fails and POST /bootstrap/master-admin returns 401).
 *
 * It produces exactly what the bootstrap endpoint would: a users row with
 * is_master_admin=1 (role 'staff') plus a PBKDF2 user_credentials row. The
 * password hash format mirrors src/lib/crypto.ts and is self-tested before
 * writing.
 *
 * Config (env vars):
 *   UFCIM_ADMIN_EMAIL       required for remote; default admin@ufcim.dev (local)
 *   UFCIM_ADMIN_PASSWORD    required for remote; default Admin@dev1234   (local)
 *   UFCIM_ADMIN_NAME        default Master Admin
 *   UFCIM_ADMIN_DEPARTMENT  default iaud  (MUST be an existing departments.id slug)
 *
 * Usage:
 *   node scripts/create-admin-direct.mjs
 *   # then apply, e.g. local dev:
 *   npx wrangler d1 execute ufcim-db-dev --local  --env dev        --file=scripts/seed_master_admin.sql
 *   # or remote prod:
 *   npx wrangler d1 execute ufcim-db     --remote --env production --file=scripts/seed_master_admin.sql
 *
 * ⚠️  The generated SQL embeds a credential hash — it is gitignored. Never commit it.
 */

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const admin = {
  email: process.env.UFCIM_ADMIN_EMAIL ?? 'admin@ufcim.dev',
  password: process.env.UFCIM_ADMIN_PASSWORD ?? 'Admin@dev1234',
  name: process.env.UFCIM_ADMIN_NAME ?? 'Master Admin',
  department: process.env.UFCIM_ADMIN_DEPARTMENT ?? 'iaud',
};

// Mirror the app's password policy (src/validators/auth.schema.ts).
if (admin.password.length < 10 || !/[A-Za-z]/.test(admin.password) || !/[0-9]/.test(admin.password)) {
  console.error('UFCIM_ADMIN_PASSWORD must be ≥10 chars and contain at least one letter and one digit.');
  process.exit(1);
}

// ── PBKDF2 helpers (ports of src/lib/crypto.ts, using Node's WebCrypto) ───────
async function hashPassword(plaintext) {
  const salt = new Uint8Array(16);
  crypto.getRandomValues(salt);
  const keyMaterial = await crypto.subtle.importKey('raw', new TextEncoder().encode(plaintext), 'PBKDF2', false, ['deriveBits']);
  const derived = await crypto.subtle.deriveBits({ name: 'PBKDF2', hash: 'SHA-256', salt, iterations: 100_000 }, keyMaterial, 256);
  const saltB64 = btoa(String.fromCharCode(...salt));
  const hashB64 = btoa(String.fromCharCode(...new Uint8Array(derived)));
  return `pbkdf2$100000$${saltB64}$${hashB64}`;
}

async function verifyPassword(plaintext, encoded) {
  const [scheme, iterStr, saltB64, hashB64] = encoded.split('$');
  if (scheme !== 'pbkdf2') return false;
  const salt = Uint8Array.from(atob(saltB64), (c) => c.charCodeAt(0));
  const expected = Uint8Array.from(atob(hashB64), (c) => c.charCodeAt(0));
  const keyMaterial = await crypto.subtle.importKey('raw', new TextEncoder().encode(plaintext), 'PBKDF2', false, ['deriveBits']);
  const derived = new Uint8Array(await crypto.subtle.deriveBits({ name: 'PBKDF2', hash: 'SHA-256', salt, iterations: parseInt(iterStr, 10) }, keyMaterial, expected.length * 8));
  return derived.length === expected.length && derived.every((b, i) => b === expected[i]);
}

const sqlStr = (s) => `'${s.replace(/'/g, "''")}'`;

async function main() {
  const now = new Date().toISOString();
  const id = crypto.randomUUID();

  const hash = await hashPassword(admin.password);
  if (!(await verifyPassword(admin.password, hash))) {
    throw new Error('Self-test failed: hash does not verify');
  }

  const target = `(SELECT id FROM users WHERE email = ${sqlStr(admin.email)})`;

  const sql = `-- ============================================================================
-- Master-admin account — generated by scripts/create-admin-direct.mjs
-- Email: ${admin.email}   Department: ${admin.department}
-- Equivalent to POST /bootstrap/master-admin (role staff, is_master_admin=1).
--
-- ⚠️  Do NOT hand-edit the password hash. Re-run the generator to change it.
-- ⚠️  Depends on the department slug existing (apply scripts/seed.sql first).
-- ============================================================================

-- ── Remove any prior copy of this account, dependents first (D1 enforces FKs) ──
DELETE FROM refresh_tokens   WHERE user_id IN ${target};
DELETE FROM user_credentials WHERE user_id IN ${target};
DELETE FROM users            WHERE email = ${sqlStr(admin.email)};

-- ── Recreate the account ──
INSERT INTO users (id, name, registration, role, department, email, is_master_admin, disabled_at, deleted_at, created_at, updated_at) VALUES
  (${sqlStr(id)}, ${sqlStr(admin.name)}, NULL, 'staff', ${sqlStr(admin.department)}, ${sqlStr(admin.email)}, 1, NULL, NULL, ${sqlStr(now)}, ${sqlStr(now)});

INSERT INTO user_credentials (user_id, password_hash, password_updated_at, failed_attempts, locked_until) VALUES
  (${sqlStr(id)}, ${sqlStr(hash)}, ${sqlStr(now)}, 0, NULL);
`;

  const outPath = join(dirname(fileURLToPath(import.meta.url)), 'seed_master_admin.sql');
  await writeFile(outPath, sql, 'utf8');

  console.log(`✓ Wrote ${outPath}`);
  console.log(`  ${admin.email} — master admin (role staff), dept "${admin.department}" (hash self-verified)`);
}

main().catch((err) => { console.error(err.message); process.exit(1); });
