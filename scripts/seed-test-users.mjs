/**
 * Generates scripts/seed_test_users.sql — one shared-password test account per
 * role for the closed POC. The SQL is FK-safe and idempotent: it removes any
 * prior copies of these accounts (and their dependent rows) before recreating
 * them, so it can be re-run against any environment.
 *
 * Passwords are PBKDF2 hashes (see src/lib/crypto.ts) — this script reproduces
 * that exact format in Node and self-tests the round-trip before writing.
 *
 * Config:
 *   UFCIM_TEST_PASSWORD   shared password for all accounts (default senha123456;
 *                         must satisfy the policy: ≥10 chars, ≥1 letter, ≥1 digit)
 *
 * Usage:
 *   node scripts/seed-test-users.mjs
 *   UFCIM_TEST_PASSWORD='outraSenha123' node scripts/seed-test-users.mjs
 *   # then apply with wrangler, e.g. for local dev:
 *   npx wrangler d1 execute ufcim-db-dev --local --env dev --file=scripts/seed_test_users.sql
 *
 * ⚠️  TEST ONLY — shared, weak credentials. Never seed these into a real tenant.
 */

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const PASSWORD = process.env.UFCIM_TEST_PASSWORD ?? 'senha123456';

// Mirror the app's password policy (src/validators/auth.schema.ts) so seeded
// accounts could also be created through the real bootstrap/invitation flow.
if (PASSWORD.length < 10 || !/[A-Za-z]/.test(PASSWORD) || !/[0-9]/.test(PASSWORD)) {
  console.error('UFCIM_TEST_PASSWORD must be ≥10 chars and contain at least one letter and one digit.');
  process.exit(1);
}

// department values must be existing departments.id slugs (see scripts/seed.sql).
// The closed POC only covers IAUD, so every test account lives in that department.
const IAUD = 'iaud';
const TEST_USERS = [
  { id: '00000000-0000-0000-0000-0000000000a1', email: 'aluno@teste.com',       name: 'Aluno Teste',       role: 'student',     department: IAUD },
  { id: '00000000-0000-0000-0000-0000000000a2', email: 'professor@teste.com',   name: 'Professor Teste',   role: 'professor',   department: IAUD },
  { id: '00000000-0000-0000-0000-0000000000a3', email: 'funcionario@teste.com', name: 'Funcionário Teste', role: 'staff',       department: IAUD },
  { id: '00000000-0000-0000-0000-0000000000a4', email: 'manutencao@teste.com',  name: 'Manutenção Teste',  role: 'maintenance', department: IAUD },
];

// ── PBKDF2 helpers (ports of src/lib/crypto.ts, using Node's WebCrypto) ───────
async function hashPassword(plaintext) {
  const salt = new Uint8Array(16);
  crypto.getRandomValues(salt);
  const keyMaterial = await crypto.subtle.importKey('raw', new TextEncoder().encode(plaintext), 'PBKDF2', false, ['deriveBits']);
  const derived = await crypto.subtle.deriveBits({ name: 'PBKDF2', hash: 'SHA-256', salt, iterations: 100_000 }, keyMaterial, 256);
  const saltB64 = btoa(String.fromCharCode(...salt));
  const hashB64 = btoa(String.fromCharCode(...new Uint8Array(derived)));
  return `pbkdf2$100000$${saltB64}$${hashB64}`;
}

async function verifyPassword(plaintext, encoded) {
  const [scheme, iterStr, saltB64, hashB64] = encoded.split('$');
  if (scheme !== 'pbkdf2') return false;
  const salt = Uint8Array.from(atob(saltB64), (c) => c.charCodeAt(0));
  const expected = Uint8Array.from(atob(hashB64), (c) => c.charCodeAt(0));
  const keyMaterial = await crypto.subtle.importKey('raw', new TextEncoder().encode(plaintext), 'PBKDF2', false, ['deriveBits']);
  const derived = new Uint8Array(await crypto.subtle.deriveBits({ name: 'PBKDF2', hash: 'SHA-256', salt, iterations: parseInt(iterStr, 10) }, keyMaterial, expected.length * 8));
  return derived.length === expected.length && derived.every((b, i) => b === expected[i]);
}

const sqlStr = (s) => `'${s.replace(/'/g, "''")}'`;

async function main() {
  const now = new Date().toISOString();
  const emailList = TEST_USERS.map((u) => sqlStr(u.email)).join(', ');
  const targets = `(SELECT id FROM users WHERE email IN (${emailList}))`;

  const rows = [];
  for (const u of TEST_USERS) {
    const hash = await hashPassword(PASSWORD);
    if (!(await verifyPassword(PASSWORD, hash))) {
      throw new Error(`Self-test failed: hash does not verify for ${u.email}`);
    }
    rows.push({ ...u, hash });
  }

  const userValues = rows.map((u) =>
    `  (${sqlStr(u.id)}, ${sqlStr(u.name)}, NULL, ${sqlStr(u.role)}, ${sqlStr(u.department)}, ${sqlStr(u.email)}, 0, NULL, NULL, ${sqlStr(now)}, ${sqlStr(now)})`
  ).join(',\n');

  const credValues = rows.map((u) =>
    `  (${sqlStr(u.id)}, ${sqlStr(u.hash)}, ${sqlStr(now)}, 0, NULL)`
  ).join(',\n');

  const sql = `-- ============================================================================
-- TEST-ONLY user accounts for the closed POC — generated by scripts/seed-test-users.mjs
-- Shared password for ALL accounts: ${PASSWORD}
-- Accounts: ${TEST_USERS.map((u) => `${u.email} (${u.role})`).join(', ')}
--
-- ⚠️  Do NOT hand-edit the password hashes. Re-run the generator to change them.
-- ⚠️  Depends on baseline departments existing (apply scripts/seed.sql first).
-- ============================================================================

-- ── Remove any prior copies of these accounts, dependents first (D1 enforces FKs) ──
DELETE FROM reservations       WHERE user_id        IN ${targets};
DELETE FROM blockings          WHERE created_by     IN ${targets};
DELETE FROM notifications      WHERE user_id        IN ${targets};
DELETE FROM audit_logs         WHERE user_id        IN ${targets};
DELETE FROM equipment_reports  WHERE reported_by    IN ${targets};
UPDATE equipment_reports SET acknowledged_by = NULL WHERE acknowledged_by IN ${targets};
DELETE FROM space_managers     WHERE user_id        IN ${targets} OR assigned_by IN ${targets};
UPDATE reservations SET recurrence_id = NULL WHERE recurrence_id IN (SELECT id FROM recurrences WHERE created_by IN ${targets});
DELETE FROM recurrences        WHERE created_by     IN ${targets};
DELETE FROM invitations        WHERE invited_by     IN ${targets};
UPDATE invitations SET accepted_user_id = NULL WHERE accepted_user_id IN ${targets};
UPDATE equipment SET updated_by = NULL WHERE updated_by IN ${targets};
DELETE FROM refresh_tokens      WHERE user_id       IN ${targets};
DELETE FROM user_credentials    WHERE user_id       IN ${targets};
DELETE FROM users               WHERE email         IN (${emailList});

-- ── Recreate the accounts ──
INSERT INTO users (id, name, registration, role, department, email, is_master_admin, disabled_at, deleted_at, created_at, updated_at) VALUES
${userValues};

INSERT INTO user_credentials (user_id, password_hash, password_updated_at, failed_attempts, locked_until) VALUES
${credValues};
`;

  const outPath = join(dirname(fileURLToPath(import.meta.url)), 'seed_test_users.sql');
  await writeFile(outPath, sql, 'utf8');

  console.log(`✓ Wrote ${outPath}`);
  console.log(`  ${rows.length} accounts, shared password "${PASSWORD}" (hashes self-verified)`);
  for (const u of rows) console.log(`    • ${u.email.padEnd(22)} ${u.role}`);
}

main().catch((err) => { console.error(err.message); process.exit(1); });
